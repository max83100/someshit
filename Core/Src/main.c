static uint16_t getChecksum(void* buffer, uint32_t len)
{
    uint16_t result = 0;
    for(uint32_t z=0;z<len/2;z++)
        result ^= ((uint16_t*)buffer)[z] + z;
    if (len%2)
        result ^= ((uint8_t*)buffer)[len-1]+len;
    return result;
}

